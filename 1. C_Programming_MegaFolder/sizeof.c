
#include<stdio.h>
int main()
{
    int i;
    float f;
    double d;
    char c;
    printf("size of int = %d bytes\n",sizeof(i));
    printf("size of float = %f bytes\n",sizeof(f));
    printf("size of double = %lf bytes\n",sizeof(d));
    printf("size of char = %c bytes\n",sizeof(c));


    return 0;
}
